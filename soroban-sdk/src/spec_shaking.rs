// --- Reference implementations ----------------------------------------------
//
// References carry no spec of their own, so they forward directly to the
// inner type's MARKER_NODE rather than emit a wrapper node.

impl<T: SpecShakingMarker> SpecShakingMarker for &T {
    const MARKER_NODE: *const MarkerNode = T::MARKER_NODE;
}

impl<T: SpecShakingMarker> SpecShakingMarker for &mut T {
    const MARKER_NODE: *const MarkerNode = T::MARKER_NODE;
}

// --- Option / Result --------------------------------------------------------

impl<T: SpecShakingMarker> SpecShakingMarker for Option<T> {
    // Single-child: forward directly, no wrapper node.
    const MARKER_NODE: *const MarkerNode = T::MARKER_NODE;
}

impl<T: SpecShakingMarker, E: SpecShakingMarker> SpecShakingMarker for Result<T, E> {
    const MARKER_NODE: *const MarkerNode = &MarkerNode {
        marker: [0u8; 14],
        children: &[T::MARKER_NODE, E::MARKER_NODE],
    } as *const MarkerNode;
}

