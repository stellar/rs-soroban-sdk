//! SpecShakingMarker is an internal trait used to ensure that contract type
//! specs are included in the WASM binary when types are used at contract
//! boundaries.
//!
//! The trait is consulted at three external boundary points:
//! 1. Contract function input parameters
//! 2. Contract function return values
//! 3. Event publishing
//!
//! Types that are only used internally (storage, cross-contract calls) do not
//! need their specs included, so the trait is NOT called from general TryFromVal
//! conversions.
//!
//! Every type that implements the trait exposes a `*const MarkerNode` via the
//! associated `MARKER_NODE` const. Nodes form a static graph via their
//! `children` slice. At the contract-fn boundary, a single `read_volatile` on a
//! pointer to the root `MARKER_NODE` roots the graph; rustc/LLVM and the WASM
//! linker then keep every transitively-referenced node (and its 14-byte
//! `SpEcV1…` marker bytes) alive through dead-code elimination. Types that
//! aren't reached from any boundary get DCE'd along with their markers, which
//! is the signal the post-build tool uses to decide which `contractspecv0`
//! entries to strip.
//!
//! Cycles in the type graph (e.g. `struct S { v: Vec<S> }`) are handled
//! natively. Children are stored as raw `*const MarkerNode` rather than
//! `&'static MarkerNode` specifically to break rustc's const-evaluation
//! cycle detector: evaluating a `&'static X` reference in a `const` expression
//! triggers evaluation of `X`'s initializer, but evaluating a `*const X`
//! (produced by either `&raw const X` on a named static or `&X {…} as *const X`
//! on a promoted static) does not. At link time, both forms resolve to the
//! same data-section addresses.

/// A node in the spec-shaking reachability graph.
///
/// Every `SpecShakingMarker` impl returns a `*const MarkerNode`. The
/// `marker` field holds the 14-byte `SpEcV1…` marker for types that have a
/// spec entry (user types generated by `#[contracttype]`/`#[contractevent]`/
/// `#[contracterror]`); it is all-zero for nodes that exist only to propagate
/// reachability to child types (containers, references, primitives).
#[doc(hidden)]
#[repr(C)]
pub struct MarkerNode {
    pub marker: [u8; 14],
    pub children: &'static [*const MarkerNode],
}

// MarkerNode contains raw pointers which aren't `Sync` by default. Safe here
// because the pointers are never dereferenced at runtime — they exist purely
// to keep statics reachable through linker DCE. The data is also immutable.
#[doc(hidden)]
unsafe impl Sync for MarkerNode {}

/// Shared sentinel for primitives and other types that carry no spec entry
/// and have no children. One static, referenced from many impls.
#[doc(hidden)]
pub static EMPTY_MARKER_NODE: MarkerNode = MarkerNode {
    marker: [0u8; 14],
    children: &[],
};

/// Trait for types that may include their spec in the WASM binary.
///
/// This trait is used internally by the SDK to ensure contract type specs
/// are included when types are used at external boundaries (function params,
/// return values, events).
///
/// Types with `#[contracttype]`, `#[contractevent]`, or `#[contracterror]`
/// implement this by emitting a `MarkerNode` containing their own 14-byte
/// marker and pointers to child types' nodes. All other types (primitives,
/// built-in SDK types, containers) also implement the trait with the same
/// shape — primitives and empty containers share [`EMPTY_MARKER_NODE`]; non-
/// empty containers emit a wrapper node pointing at their element type(s).
#[doc(hidden)]
pub trait SpecShakingMarker {
    const MARKER_NODE: *const MarkerNode;
}

