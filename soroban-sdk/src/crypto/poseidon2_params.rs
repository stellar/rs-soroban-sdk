use crate::{bytesn, vec, Env, Vec, U256};

// The sbox degree do not change for BN254 and BLS12_381
pub const SBOX_D: u32 = 5;

// The partial rounds and the full rounds do not change for t = 2,3,4. This may
// not be true if we want to support higher state size in the future
pub fn get_rounds_f(t: u32) -> u32 {
    match t {
        2 | 3 | 4 => 8,
        _ => unimplemented!("Poseidon2 preset parameters only support t = 2, 3, 4. You can supply your own parameters and call CryptoHazmat::poseidon2_permutation"),
    }
}

pub fn get_rounds_p(t: u32) -> u32 {
    match t {
        2 | 3 | 4 => 56,
        _ => unimplemented!("Poseidon2 preset parameters only support t = 2, 3, 4. You can supply your own parameters and call CryptoHazmat::poseidon2_permutation"),
    }
}

